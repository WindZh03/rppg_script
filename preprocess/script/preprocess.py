def main():
    args = parser.parse_args()
    suffix = f"_s{args.store_size}" if args.store_size is not None else ""
    if args.dataset_name == "UBFC-rPPG":
        subjects = sorted(os.listdir(args.video_dir))
        print("subjects:", subjects)
        print("number of subjects:", len(subjects))

        for subject in tqdm(subjects, desc="subject"):
            video_path = os.path.join(args.video_dir, subject, "vid.avi")
            landmark_path = os.path.join(args.landmark_dir, subject, "vid.csv")
            json_path = os.path.join(args.json_dir, subject, "ground_truth.txt")
            h5_path = os.path.join(args.h5_dir, subject, f"vid{suffix}.h5")

            h5_subdir = os.path.join(args.h5_dir, subject)
            if not os.path.exists(h5_subdir):
                os.makedirs(h5_subdir)

            openface_h5(args.dataset_name, video_path, landmark_path, json_path, h5_path, store_size=args.store_size)

    elif args.dataset_name == "PURE":
        sessions = sorted(os.listdir(args.video_dir))
        print("sessions:", sessions)
        print("number of sessions:", len(sessions))

        for session in tqdm(sessions, desc="session"):
            video_path = os.path.join(args.video_dir, session, session)
            landmark_path = os.path.join(args.landmark_dir, session, session + ".csv")
            json_path = os.path.join(args.json_dir, session, session + ".json")
            h5_path = os.path.join(args.h5_dir, session, f"{session}{suffix}.h5")

            h5_subdir = os.path.join(args.h5_dir, session)
            if not os.path.exists(h5_subdir):
                os.makedirs(h5_subdir)

            openface_h5(args.dataset_name, video_path, landmark_path, json_path, h5_path, store_size=args.store_size)

    elif args.dataset_name == "BUAA":
        subject_folders = get_subfolders(args.video_dir)
        print("number of subjects:", len(subject_folders))
        for subject_folder in tqdm(subject_folders, desc="subject"):
            subject = os.path.basename(subject_folder)

            session_folders = get_subfolders(subject_folder)
            for session_folder in session_folders:
                session = os.path.basename(session_folder)
                if session not in ["lux_10.0", "lux_15.8", "lux_25.1", "lux_39.8", "lux_63.1", "lux_100.0"]:
                    continue

                video_paths = [os.path.join(session_folder, f) for f in os.listdir(session_folder) if f.endswith(".avi")]
                if not video_paths:
                    print(f"There are no .avi files in {session_folder}.")

                video_path = video_paths[0]
                video_name = os.path.basename(video_path)
                video_name = os.path.splitext(video_name)[0]

                landmark_path = os.path.join(args.landmark_dir, subject, session, video_name + ".csv")
                json_path = os.path.join(args.json_dir, subject, session)
                h5_path = os.path.join(args.h5_dir, subject, session, f"{video_name}{suffix}.h5")

                h5_subdir = os.path.join(args.h5_dir, subject, session)
                if not os.path.exists(h5_subdir):
                    os.makedirs(h5_subdir)

                PPGData_mat = os.path.join(json_path, "PPGData.mat")
