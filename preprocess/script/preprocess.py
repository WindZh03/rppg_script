def get_subfolders(folder: str):
    # Get the full paths of the immediate subdirectories in a folder
    if not os.path.isdir(folder):
        raise ValueError(f"{folder} is not a valid folder path.")

    subfolders = [os.path.join(folder, name) for name in os.listdir(folder) if os.path.isdir(os.path.join(folder, name))]

    return subfolders


def my_interp(data, original_len, target_len):
    # Create target index
    original_indices = np.linspace(0, 1, original_len)
    target_indices = np.linspace(0, 1, target_len)

    # Use linear interpolation
    interpolated_data = np.interp(target_indices, original_indices, data)
    return interpolated_data


def read_ground_truth(dataset_name, total_num_frame, json_path):
    if dataset_name == "UBFC-rPPG":
        bvp, hr, video_t = np.loadtxt(json_path)
        return video_t, bvp, hr

    elif dataset_name == "PURE":
        with open(json_path, "r") as infile:
            gt_data = json.load(infile)
        # Read video timestamps
        video_t = []
        for sample in gt_data["/Image"]:
            video_t.append(sample["Timestamp"])
        # Read oximeter data
        wave_t = []
        bvp = []
        hr = []
        for sample in gt_data["/FullPackage"]:
            wave_t.append(sample["Timestamp"])
            bvp.append(sample["Value"]["waveform"])
            hr.append(sample["Value"]["pulseRate"])
        video_t = np.array(video_t) * 1e-9
        wave_t = np.array(wave_t) * 1e-9
        bvp = np.array(bvp)
        bvp = np.interp(video_t, wave_t, bvp)
        hr = np.array(hr)
        hr = np.interp(video_t, wave_t, hr)
        return video_t, bvp, hr

    elif dataset_name == "BUAA":
        data_path = os.path.join(json_path, "PPGData.mat")
        Num = total_num_frame

        pulse = scio.loadmat(data_path)["PPG"]["data"][0][0]
        pulse = np.array(np.array(pulse).astype("float32")).reshape(-1)

        Time = np.linspace(0, Num, len(pulse))
        CSI_Time = np.linspace(0, Num, Num)

        t = interpolate.splrep(Time, pulse)
        pulse_csi = interpolate.splev(CSI_Time, t)

        LPF = 0.7  # low cutoff frequency(Hz) - specified as 40bpm(~0.667Hz) in reference
        HPF = 2.5  # high cutoff frequency(Hz) - specified as 240bpm(~4.0Hz) in reference
        NyquistF = 15  # 15fps
        FS = 30  # 30fps
        B, A = signal.butter(3, [LPF / NyquistF, HPF / NyquistF], "bandpass")
        pulse_csi = (pulse_csi - np.min(pulse_csi)) / (np.max(pulse_csi) - np.min(pulse_csi))
        EXG2_f = signal.filtfilt(B, A, pulse_csi)
        EXG2_f = (EXG2_f - np.min(EXG2_f)) / (np.max(EXG2_f) - np.min(EXG2_f))

        # total_num_frame = 1800
        video_t = CSI_Time
        bvp = pulse_csi
        hr = np.full_like(bvp, 60)  # ! fake
        return video_t, bvp, hr


def openface_h5(dataset_name, video_path, landmark_path, json_path, h5_path, store_size=128):
    """
    crop face from OpenFace landmarks and save a video as .h5 file.

    video_path: the face video path
    landmark_path: landmark .csv file generated by OpenFace.
    h5_path: the path to save the h5_file
    store_size: the cropped face is resized to 128 (default).
    """
    if dataset_name == "UBFC-rPPG":
        landmark = pd.read_csv(landmark_path)

        with h5py.File(h5_path, "w") as f:
            total_num_frame = len(landmark)
            cap = cv2.VideoCapture(video_path)
            for frame_num in range(total_num_frame):
                if landmark["success"][frame_num]:
                    lm_x = []
                    lm_y = []
                    for lm_num in range(68):
                        lm_x.append(landmark["x_%d" % lm_num][frame_num])
                        lm_y.append(landmark["y_%d" % lm_num][frame_num])

                    lm_x = np.array(lm_x)
                    lm_y = np.array(lm_y)

                    minx = np.min(lm_x)
                    maxx = np.max(lm_x)
                    miny = np.min(lm_y)
                    maxy = np.max(lm_y)

                    y_range_ext = (maxy - miny) * 0.2
                    miny = miny - y_range_ext

                    cnt_x = np.round((minx + maxx) / 2).astype("int")
                    cnt_y = np.round((maxy + miny) / 2).astype("int")

                    break

            bbox_size = np.round(1.5 * (maxy - miny)).astype("int")

            # init dataset in h5
            if store_size == None:
                store_size = bbox_size

            imgs = f.create_dataset(
                "imgs",
                shape=(total_num_frame, store_size, store_size, 3),
                dtype="uint8",
                chunks=(1, store_size, store_size, 3),
                compression="gzip",
                compression_opts=4,
            )

            for frame_num in range(total_num_frame):
                if landmark["success"][frame_num]:
                    lm_x_ = []
                    lm_y_ = []
                    for lm_num in range(68):
                        lm_x_.append(landmark["x_%d" % lm_num][frame_num])
                        lm_y_.append(landmark["y_%d" % lm_num][frame_num])

                    lm_x_ = np.array(lm_x_)
                    lm_y_ = np.array(lm_y_)

                    lm_x = 0.9 * lm_x + 0.1 * lm_x_
                    lm_y = 0.9 * lm_y + 0.1 * lm_y_

                    minx = np.min(lm_x)
                    maxx = np.max(lm_x)
                    miny = np.min(lm_y)
                    maxy = np.max(lm_y)

                    y_range_ext = (maxy - miny) * 0.2
                    miny = miny - y_range_ext

                    cnt_x = np.round((minx + maxx) / 2).astype("int")
                    cnt_y = np.round((maxy + miny) / 2).astype("int")

                ret, frame = cap.read()
                frame = cv2.cvtColor(frame, cv2.COLOR_BGR2RGB)

                if not ret:
                    print("Can't receive frame (stream end?). Exiting ...")
                    break

                # for bbox
                bbox_half_size = int(bbox_size / 2)

                face = np.take(frame, range(cnt_y - bbox_half_size, cnt_y - bbox_half_size + bbox_size), 0, mode="clip")
                face = np.take(face, range(cnt_x - bbox_half_size, cnt_x - bbox_half_size + bbox_size), 1, mode="clip")

                if store_size == bbox_size:
                    imgs[frame_num] = face
                else:
                    imgs[frame_num] = cv2.resize(face, (store_size, store_size))

            cap.release()

            wave_t, bvp, hr = read_ground_truth(dataset_name, total_num_frame, json_path)

            # Add BVP to the existing .h5 file
            with h5py.File(h5_path, "a") as f:
                f.create_dataset("wave_t", data=wave_t)
                f.create_dataset("bvp", data=bvp)
